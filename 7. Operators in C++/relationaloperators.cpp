#include<iostream>
using namespace std;
int main(){
    int a=10,b=12;
    if(a==10){
        cout<<"a="<<a<<endl;
    }
    if(a!=b){
        cout<<"a!=b"<<endl;
    }
    if(a>b){
        cout<<"a>b"<<endl;
    }
    if(a<b){
        cout<<"a<b"<<endl;
    }
    if(a>=b){
        cout<<"a>=b"<<endl;
    }
    if(a<=b){
        cout<<"a<=b"<<endl;
    }
    return 0;
}